function nineBoxPosition(dg, vg) {
  // cols: desempenho 1-2=col1, 3=col2, 4-5=col3
  // rows: valores 1-2=row1(bottom), 3=row2(mid), 4-5=row3(top)
  const col = dg <= 2 ? 0 : dg === 3 ? 1 : 2;
  const row = vg <= 2 ? 2 : vg === 3 ? 1 : 0;
  return { col, row };
}

